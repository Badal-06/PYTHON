str = "hello_badal"
# Indexing in String :-
print(str[4]) # o
print(str[-4]) # a
print(str[1:5]) # ello
print(str[:4]) # hell
print(str[0:4]) # hell
print(str[0:]) # hello_badal
print(str[0:len(str)]) # hello_badal
print(str[0:12]) # hello_badal
print(str[-1:-3]) # nothing due to indexing work from left to right 
print(str[-3:-1]) # da