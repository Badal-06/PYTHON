# Function to calculate the product of two numbers
def calc_pro(a, b):
    # Multiply a and b, store the result in p
    p = a * b
    # Print the product
    print(p)
    # Return the product value to the caller
    return p

# Call the function with 287 and 965 as arguments
calc_pro(287, 965)

# Call the function with 100 and 200 as arguments
calc_pro(100, 200)
